from random import randrange


def imprimir_mensagem_abertura():
    print('=' * 30)
    print('BEM VINDO AO JOGO DA FORCA'.center(30))
    print('=' * 30)


def escolher_palavra_secreta():
    palavras = []
    with open('./services/palavras.txt', 'r', encoding='utf-8') as arquivo:
        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    numero = randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta


def inicializar_letras_acertadas(palavra):
    return ['_' for letra in palavra]


def pedir_chute():
    chute = input('Qual letra? ').upper().strip()
    return chute


def marcar_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1

